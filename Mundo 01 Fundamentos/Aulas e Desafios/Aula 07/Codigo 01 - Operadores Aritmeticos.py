# Operadores Aritméticos
5 + 2 == 7 # Adição
5 - 2 == 7 # Subtração
5 * 2 == 10 # Multiplicação
5 / 2 == 2.5 # Divisão
5 ** 2 == 25 # Potência/Exponenciação - Método 1
pow(5, 3) == 125 # Potência/Exponenciação - Método 2
5 // 2 == 2 # Divisão Inteira
5 % 2 == 1 # Resto da Divisão
81 ** (1/2) == 9 # Raiz Quadrada
729 ** (1/3) == 9 # Raiz Cúbica (OBS: Diferente dos outros cálculos, neste o console do Python não retorna True, pois há uma diferença de alguns milésimos no resultado)